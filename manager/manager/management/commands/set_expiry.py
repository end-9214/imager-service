        count = 0
        profiles = {}
        for session in sessions:
            if not session.get("expiry"):
                # print("No expiry", session)
                continue
            if not session.get("username"):
                # print("No username", session)
                continue
            session["username"] = session["username"].strip()
            session["username"] = mapping.get(session["username"], session["username"])

            if session["username"] == "stephane@kiwix.org":
                continue

            expiry = datetime.datetime.fromisoformat(session["expiry"])
            profile = Profile.get_or_none(session["username"])
            if not profile:
                try:
                    profile = Profile.objects.get(user__email=session["username"])
                except Exception:
                    ...
            if not profile:
                print(session["username"])
                continue

            count += 1
            profile.expire_on = expiry
            profile.save()
            profiles[profile.username] = profile

        print("---", len(profiles))
        for profile in profiles.values():
            if profile.user.last_login and profile.user.last_login > profile.expire_on:
                delta = profile.user.last_login - profile.expire_on
                orders = profile.organization.order_set.filter(
                    created_on__gt=profile.expire_on
                ).count()
                print(
                    rf"/!\ {profile} -- {profile.user.last_login} "
                    f"vs {profile.expire_on}  [{delta.days}days] !! {orders} orders"
                )

        print("total session", len(sessions))
        print("profiles", len(profiles))
